    private void cbBirimItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbBirimItemStateChanged
        // TODO add your handling code here:,
        EntityManagerFactory emf=Persistence.createEntityManagerFactory("BLM104_2017_2018BaharPU");
        EntityManager em=emf.createEntityManager();
        Query q=em.createQuery("select p from Personel p where p.birimAdi=:pBirimAdi");
        q.setParameter("pBirimAdi", cbBirim.getSelectedItem());
        List<Personel> personeller=q.getResultList();
        DefaultTableModel dtm=new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"personel_id","adi"});
        for (Personel p : personeller) {
            dtm.addRow(new String[]{p.getPersonelId()+"",p.getAdi()});
        }
        tblPersonel.setModel(dtm);
    }//GEN-LAST:event_cbBirimItemStateChanged

    private void tblPersonelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPersonelMouseClicked
        // TODO add your handling code here:
        if (tblPersonel.getSelectedRow()==-1) return;
        
        
       
        EntityManagerFactory emf=Persistence.createEntityManagerFactory("BLM104_2017_2018BaharPU");
        EntityManager em=emf.createEntityManager();
        Query q=em.createQuery("select t from PersonelTelefon t where t.pId=:pId");
        q.setParameter("pId",  Integer.parseInt((String)tblPersonel.getValueAt(tblPersonel.getSelectedRow(),0)));
        List<PersonelTelefon> ptler=q.getResultList();
        DefaultTableModel dtm=new DefaultTableModel();
        dtm.setColumnIdentifiers(new String[]{"telefonadi","fiyat"});
        for (PersonelTelefon t : ptler) {
            dtm.addRow(new String[]{getTelefonAdi(t.getTId()),t.getAlimFiyat()+""});
        }
        tblTelefon.setModel(dtm);
    }//GEN-LAST:event_tblPersonelMouseClicked
    String getTelefonAdi(int telefonId){
        EntityManagerFactory emf=Persistence.createEntityManagerFactory("BLM104_2017_2018BaharPU");
        EntityManager em=emf.createEntityManager();
        Query q=em.createQuery("select t from Telefon t where t.telId=:pTelId");
        q.setParameter("pTelId", telefonId);
        List<Telefon> ptler=q.getResultList();
        return ptler.get(0).getTelAdi();
    }
